#include "libft.h"
#include <stdlib.h>

/////////////////////////////////////////////////////////////////////////////
// duplicate str in memory and return pointer to the copy
char	*ft_strdup(const char *s1)
{
	char	*tab;
	int		i;

	i = 0;
	tab = malloc(sizeof(char) * (ft_strlen(s1) + 1));
	if (!tab)
		return (NULL);
	while (s1[i])
	{
		tab[i] = s1[i];
		i++;
	}
	tab[i] = '\0';
	return (tab);
}
