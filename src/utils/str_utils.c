// count nb a char for newStr of fixParam
int	count_clean(char *s)
{
	int	len;
	int	i;

	len = 0;
	i = 0;
	while (s[i])
	{
		if (!is_ws(s[i]))
			len++;
		i++;
	}
	return (len);
}
