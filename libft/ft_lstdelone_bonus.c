#include "libft.h"
#include <stdlib.h>

/////////////////////////////////////////////////////////////////////////////
// clean one link without touch next
void	ft_lstdelone(t_list *lst, void (*del)(void *))
{
	if (!lst)
		return ;
	if (del)
		del(lst->content);
	free(lst);
}
