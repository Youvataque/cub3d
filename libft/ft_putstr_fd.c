#include "libft.h"
#include <unistd.h>

/////////////////////////////////////////////////////////////////////////////
// print str on selected fd
void	ft_putstr_fd(char *s, int fd)
{
	size_t	i;

	i = 0;
	while (i < ft_strlen(s))
	{
		write(fd, &s[i], 1);
		i++;
	}
}
