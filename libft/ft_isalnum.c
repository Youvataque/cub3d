#include "libft.h"

/////////////////////////////////////////////////////////////////////////////
// check if char is alphanum
int	ft_isalnum(int c)
{
	if (ft_isalpha(c) == 1 || ft_isdigit(c) == 1)
		return (1);
	return (0);
}
